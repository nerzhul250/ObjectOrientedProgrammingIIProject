package mundo;

public interface Parametrizable {
	/**
	 * Método que calcula la posición x del objeto parametrizable
	 * @param t parámetro t
	 * @return posición x según el parámetro t
	 */
	double darPosicionX(double t);
	/**
	 * Método que calcula la posición y del objeto parametrizable
	 * @param t parámetor
	 * @return posición Y según el parámetro t
	 */
	double darPosicionY(double t);
}
